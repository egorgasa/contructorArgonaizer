export function getConstructorPreset(
  id: ConstructorPresetId,
): ConstructorPreset | undefined {
  return CONSTRUCTOR_PRESETS.find((p) => p.id === id);
}

// ---- Confirmation gate -----------------------------------------------------

/**
 * True when the user has hand-made constructor work that a preset would
 * replace: any user-authored design element, or an image element carrying data.
 * System-managed elements (holes / fasteners / decor / textDecoration) don't
 * count — they're derived from toggles and re-created by presets anyway. Used by
 * the picker to decide whether to show a "this will replace your settings"
 * confirmation before applying.
 */
export function presetApplyNeedsConfirm(
  current: Pick<PrintRequestInput, "design">,
): boolean {
  const elements = current.design?.elements;
  if (!Array.isArray(elements)) return false;
  return elements.some((el) => {
    const source = el?.source ?? "user";
    if (source === "user") return true;
    // An image dropped in by the user carries data worth protecting.
    if (el?.type === "image" && typeof el.imageDataUrl === "string" && el.imageDataUrl.length > 0) {
      return true;
    }
    return false;
  });
}

// ---- Apply -----------------------------------------------------------------

/** Build the design.elements a preset implies, with freshly-generated ids. */
function buildPresetElements(
  values: ConstructorPresetValues,
): DesignElement[] {
  const elements: DesignElement[] = [];

  if (values.hasHoles) {
    elements.push(createHoleElement());
  }
  if (values.hasFasteners) {
    elements.push(...createFastenerElements());
  }
  if (values.hasDecoration) {
    // Mirror the Features-step behaviour: a decorative overlay element.
    elements.push(createDesignElement("decor", "overlay"));
  }

  const text = values.appearance?.textDecoration;
  if (text?.enabled && text.text) {
    const placement = (text.placement ?? "front") as TextPlacement;
    const center = TEXT_PLACEMENT_CENTER[placement];
    elements.push(
      createTextDecorationElement({
        text: text.text,
        fillColor: text.colorHex ?? createDefaultAppearance().textDecoration.colorHex,
        x: center.x,
        y: center.y,
      }),
    );
  }

  return elements;
}

/**
 * Produce a complete `PrintRequestInput` that applies `preset` on top of the
 * user's `current` form values:
 *   1. start from the blank constructor defaults (so switching presets never
 *      leaves stale geometry behind);
 *   2. preserve the user's contact / context / material fields from `current`;
 *   3. overlay the preset's declarative `values`;
 *   4. generate the implied design elements (holes / fasteners / decor / text)
 *      and front handles with fresh ids;
 *   5. mirror the chosen base shape onto the legacy `shape` field and sync the
 *      `design.baseShape` dimensions to the numeric width/depth so every preview
 *      and server validation reads a consistent geometry.
 *
 * The result is meant to be handed to `react-hook-form`'s `reset()`. No preset
 * id is included anywhere — presets never touch the payload.
 */
export function applyConstructorPresetValues(
  current: PrintRequestInput,
  preset: ConstructorPreset,
): PrintRequestInput {
  const blank = blankConstructorFields();
  const v = preset.values;

  // (1)+(2) baseline = blank constructor fields + preserved user context.
  const next: PrintRequestInput = { ...current, ...blank };
  for (const key of PRESERVED_FIELDS) {
    // Each preserved field keeps the user's current value (already present in
    // `current`, copied above) — listed explicitly for intent / auditing.
    (next as Record<string, unknown>)[key] = current[key];
  }

  // (3) overlay simple scalar / string / boolean preset fields.
  if (v.productType !== undefined) next.productType = v.productType;
  if (v.widthMm !== undefined) next.widthMm = v.widthMm;
  if (v.depthMm !== undefined) next.depthMm = v.depthMm;
  if (v.heightMm !== undefined) next.heightMm = v.heightMm;
  if (v.wallThicknessMm !== undefined) next.wallThicknessMm = v.wallThicknessMm;
  if (v.cornerRadiusMm !== undefined) next.cornerRadiusMm = v.cornerRadiusMm;
  if (v.sectionsCount !== undefined) next.sectionsCount = v.sectionsCount;
  if (v.hasHoles !== undefined) next.hasHoles = v.hasHoles;
  if (v.holesDescription !== undefined) next.holesDescription = v.holesDescription;
  if (v.hasFasteners !== undefined) next.hasFasteners = v.hasFasteners;
  if (v.fastenersDescription !== undefined)
    next.fastenersDescription = v.fastenersDescription;
  if (v.hasDecoration !== undefined) next.hasDecoration = v.hasDecoration;
  if (v.decorationDescription !== undefined)
    next.decorationDescription = v.decorationDescription;

  // Lid: blank disabled default unless the preset enables it.
  const lid: LidSettings = { ...createDefaultLid(), ...(v.lid ?? {}) } as LidSettings;
  next.lid = lid;

  // Appearance: keep the user's colour / finish / pattern, but reset the
  // text-decoration bridge (its design element is rebuilt below) and overlay any
  // preset appearance on top.
  const currentAppearance = (current.appearance ?? createDefaultAppearance()) as Appearance;
  const mergedAppearance: Appearance = {
    ...createDefaultAppearance(),
    ...currentAppearance,
    pattern: { ...createDefaultAppearance().pattern, ...currentAppearance.pattern },
    // Default = disabled; a preset (e.g. the plaque) re-enables it just below.
    textDecoration: { ...createDefaultAppearance().textDecoration },
  };
  if (v.appearance) {
    Object.assign(mergedAppearance, {
      ...v.appearance,
      pattern: { ...mergedAppearance.pattern, ...v.appearance.pattern },
      textDecoration: {
        ...mergedAppearance.textDecoration,
        ...v.appearance.textDecoration,
      },
    });
  }
  next.appearance = mergedAppearance;

  // (5) base shape + legacy mirroring.
  const presetBaseShape = v.design?.baseShape;
  const kind: BaseShapeKind =
    (presetBaseShape?.kind as BaseShapeKind | undefined) ?? "rectangle";
  const cornerRadiusMm = next.cornerRadiusMm ?? 0;
  next.shape = baseKindToLegacyShape(kind);

  const design: DesignSettings = {
    baseShape: {
      ...DEFAULT_DESIGN_BASE_SHAPE,
      ...(presetBaseShape ?? {}),
      kind,
      widthMm: next.widthMm,
      heightMm: next.depthMm,
      cornerRadiusMm,
    },
    // (4) elements derived from the preset intent, with fresh ids.
    elements: buildPresetElements({ ...v, appearance: mergedAppearance }),
  };
  next.design = design;

  // (4) front-mounted handles, fresh ids.
  next.accessories = Array.from({ length: preset.frontHandles ?? 0 }, () =>
    createHandleAccessory({ side: "front" }),
  ) as PrintRequestInput["accessories"];

  return next;
}
