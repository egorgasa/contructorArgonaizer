// ---- Ids -------------------------------------------------------------------

export const CONSTRUCTOR_PRESET_IDS = [
  "boxWithLid",
  "sectionedOrganizer",
  "panelWithHoles",
  "textPlaque",
  "blank",
] as const;
export type ConstructorPresetId = (typeof CONSTRUCTOR_PRESET_IDS)[number];

// ---- Preset declaration ----------------------------------------------------

/**
 * Static, declarative slice of a preset. A plain `Partial<PrintRequestInput>`
 * (no `any`, no deep-partial gymnastics): a preset only ever sets a handful of
 * top-level constructor fields, and the *dynamic* bits that need generated ids
 * (design elements, accessories) are produced by `applyConstructorPresetValues`
 * from the intent already encoded here (`hasHoles`, `hasFasteners`,
 * `appearance.textDecoration.enabled`, …) plus the preset-level `frontHandles`
 * count below. Storing pre-built elements/accessories here would generate ids at
 * module-load time, which we deliberately avoid.
 */
export type ConstructorPresetValues = Partial<PrintRequestInput>;

export interface ConstructorPreset {
  id: ConstructorPresetId;
  title: string;
  description: string;
  /** Short "good for …" line shown under the description. */
  recommendedFor: string;
  /** 2–3 short tags rendered as chips on the card. */
  chips: readonly string[];
  /** Declarative static field values applied on top of the constructor defaults. */
  values: ConstructorPresetValues;
  /**
   * Number of front-mounted handles to create when this preset is applied. The
   * accessories themselves (with fresh ids) are built in
   * `applyConstructorPresetValues`, never at module load.
   */
  frontHandles?: number;
}

// ---- Fields preserved across a preset apply --------------------------------
//
// Everything the user typed that describes *who they are* / *what they want* but
// not the item geometry. A preset never silently discards these.
const PRESERVED_FIELDS = [
  "purpose",
  "usageEnvironment",
  "referenceDescription",
  "optionalReferenceLinks",
  "clientComment",
  "clientName",
  "clientEmail",
  "clientPhone",
  "preferredContactTime",
  "consentPersonalData",
  // Material catalogue choices are user context, not geometry — keep them.
  "material",
  "color",
  "strength",
] as const satisfies readonly (keyof PrintRequestInput)[];

// Normalized centres per text-face, matching the Features-step defaults. Kept
// local (a tiny domain constant) so this module stays free of any React import.
const TEXT_PLACEMENT_CENTER: Record<TextPlacement, { x: number; y: number }> = {
  front: { x: 0.5, y: 0.8 },
  back: { x: 0.5, y: 0.2 },
  left: { x: 0.3, y: 0.5 },
  right: { x: 0.7, y: 0.5 },
  top: { x: 0.5, y: 0.5 },
};

// ---- Blank (default) constructor state -------------------------------------

/**
 * Fresh constructor-field defaults — the geometry/appearance baseline a preset
 * apply (and the "blank" preset) resets to. Mirrors the constructor half of the
 * wizard's `DEFAULTS`; contact / context fields are intentionally absent here
 * because they're preserved from the current form values, not reset.
 */
function blankConstructorFields(): Pick<
  PrintRequestInput,
  | "productType"
  | "widthMm"
  | "depthMm"
  | "heightMm"
  | "wallThicknessMm"
  | "shape"
  | "cornerRadiusMm"
  | "sectionsCount"
  | "hasHoles"
  | "holesDescription"
  | "hasFasteners"
  | "fastenersDescription"
  | "hasDecoration"
  | "decorationDescription"
  | "appearance"
  | "design"
  | "accessories"
  | "lid"
> {
  return {
    productType: "" as PrintRequestInput["productType"],
    widthMm: 180,
    depthMm: 120,
    heightMm: 60,
    wallThicknessMm: 2.0,
    shape: "rectangular",
    cornerRadiusMm: 0,
    sectionsCount: 0,
    hasHoles: false,
    holesDescription: "",
    hasFasteners: false,
    fastenersDescription: "",
    hasDecoration: false,
    decorationDescription: "",
    appearance: createDefaultAppearance(),
    design: { baseShape: { ...DEFAULT_DESIGN_BASE_SHAPE }, elements: [] },
    accessories: [],
    lid: createDefaultLid(),
  };
}

